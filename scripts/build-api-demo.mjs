// Generates src/data/apiDemo.ts — the measured terms that power the interactive
// Signal Explorer on /waitlist. Real measured readings only (campaigns 03-06);
// nothing is invented. Run: node scripts/build-api-demo.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const HERE = dirname(fileURLToPath(import.meta.url));
const CAMPAIGNS = join(HERE, '..', 'src', 'data', 'campaigns');
const OUT = join(HERE, '..', 'src', 'data', 'apiDemo.ts');
const SOURCES = ['03', '04', '05', '06'];

// Curated chips shown by default in the explorer (vivid, cross-campaign, good for compare()).
const FEATURED = new Set([
  'ceasefire', 'obliterated', 'soft-landing', 'vibecession',
  'greedflation', 'america-first', 'maga-warrior', 'seed-oils',
]);

const slugify = (t) => t.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');

const bySlug = new Map();
for (const nn of SOURCES) {
  const data = JSON.parse(readFileSync(join(CAMPAIGNS, `${nn}.json`), 'utf8'));
  for (const t of data.terms || []) {
    const m = t.measurement || {};
    if (m.status !== 'measured') continue;
    const slug = slugify(t.term);
    const entry = {
      term: t.term,
      slug,
      campaign: data.number,
      campaignTitle: data.shortTitle || data.title,
      domain: t.domainTag || '',
      velocityIndex: m.velocityIndex ?? null,
      adoptionStage: m.adoptionStage ?? null,
      deltas: {
        d30: m.deltas?.d30 ?? null,
        d90: m.deltas?.d90 ?? null,
        yoy: m.deltas?.yoy ?? null,
      },
      sentiment: m.sentiment ?? null,
      inflections: (m.inflections || []).map((i) => ({ date: i.date, z: i.z })),
      coOccurrence: t.coOccurrence || [],
      retrievedAt: m.retrievedAt || '',
      featured: FEATURED.has(slug),
    };
    // dedupe by phrase: keep the most recently retrieved (e.g. "America First" C04 vs C06)
    const prev = bySlug.get(slug);
    if (!prev || entry.retrievedAt > prev.retrievedAt) bySlug.set(slug, entry);
  }
}

// Featured first (in the curated order), then the rest by campaign+term.
const order = [...FEATURED];
const all = [...bySlug.values()].map((e) => {
  const { retrievedAt, ...rest } = e;
  return rest;
});
all.sort((a, b) => {
  const ai = a.featured ? order.indexOf(a.slug) : 999;
  const bi = b.featured ? order.indexOf(b.slug) : 999;
  if (ai !== bi) return ai - bi;
  return a.campaign === b.campaign ? a.term.localeCompare(b.term) : a.campaign.localeCompare(b.campaign);
});

const header = `// AUTO-GENERATED by scripts/build-api-demo.mjs — do not edit by hand.
// Real measured readings (campaigns 03-06). Numbers are measured, never invented.
// Regenerate: node scripts/build-api-demo.mjs

export interface DemoTerm {
  term: string;
  slug: string;
  campaign: string;
  campaignTitle: string;
  domain: string;
  velocityIndex: number | null;
  adoptionStage: string | null;
  deltas: { d30: number | null; d90: number | null; yoy: number | null };
  sentiment: number | null;
  inflections: { date: string; z: number }[];
  coOccurrence: string[];
  featured: boolean;
}

export const API_DEMO_TERMS: DemoTerm[] = ${JSON.stringify(all, null, 2)};
`;

writeFileSync(OUT, header, 'utf8');
console.log(`wrote ${OUT} — ${all.length} terms (${all.filter((t) => t.featured).length} featured)`);
